 <p style="font-size:13px; font-style:normal;">
   No espírito das ideias que merecem ser espalhadas, o TED criou o programa chamado TEDx. O TEDx é um programa de eventos locais, e organizados de forma independente, que reúne pessoas para dividir uma experiência ao estilo TED. O TEDx São Paulo é uma conferência sem fins lucrativos que reunirá mais de 30 pensadores de áreas de conhecimento tão diversas quanto arte e tecnologia, ciência e negócios, para falar sobre suas melhores ideias em palestras com duração de 5 ou 15 minutos. O tema desta primeira edição do evento será: O que o Brasil tem a oferecer ao mundo hoje? O TEDx São Paulo acontecerá no dia 14 de novembro de 2009 e será gratuito.<br />
<br />
<br />


O TED surgiu em 1984 como uma conferência anual na Califórnia e já teve entre seus palestrantes Bill Clinton, Paul Simon, Bill Gates, Bono Vox, Al Gore, Michelle Obama e Philippe Starck. Apesar dos mil lugares na platéia, as inscrições esgotam-se um ano antes. Cerca de 500 das palestras estão disponíveis no site do evento e já foram acessadas por mais de 50 milhões de pessoas de 150 países. A cada ano a organização elege um pensador de destaque e repassa a ele 100 mil dólares para ele que possa realizar “Um Desejo que Vai Mudar o Mundo”. Com essas 4 ações, TED Conferece, TED Talks, TED Prize e TEDx a organização pretende transformar seu mote “ideias que merecem ser espalhadas” cada vez mais em realidade. “Acreditamos apaixonadamente no poder das ideias para mudar atitudes, vidas e, em última instância, o mundo”, dizem os organizadores do TED. Nós também. E você?</p>
